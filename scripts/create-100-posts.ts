/**
 * 메인 실행 함수
 */
async function main() {
  console.log('🚀 100개 롱테일 키워드 콘텐츠 생성 시작\n');

  // JSON 파일 읽기
  const jsonPath = path.join(process.cwd(), 'scripts', 'keywords-100.json');
  const jsonData = fs.readFileSync(jsonPath, 'utf-8');
  const keywordsData: KeywordsData = JSON.parse(jsonData);

  console.log(`📝 총 ${keywordsData.posts.length}개 포스트 생성 예정\n`);

  let created = 0;
  let skipped = 0;

  for (const post of keywordsData.posts) {
    const categoryDir = path.join(process.cwd(), 'posts', post.category);
    const filePath = path.join(categoryDir, `${post.slug}.md`);

    // 카테고리 폴더 확인/생성
    if (!fs.existsSync(categoryDir)) {
      fs.mkdirSync(categoryDir, { recursive: true });
      console.log(`📁 카테고리 폴더 생성: ${post.category}`);
    }

    // 파일 존재 여부 확인
    if (fs.existsSync(filePath)) {
      console.log(`⏭️  스킵 (이미 존재): ${post.slug}`);
      skipped++;
      continue;
    }

    // 마크다운 파일 생성
    const markdown = generateMarkdownFile(post);
    fs.writeFileSync(filePath, markdown, 'utf-8');
    console.log(`✅ 생성: ${post.category}/${post.slug}`);
    created++;
  }

  console.log('\n📊 결과 요약');
  console.log(`   생성됨: ${created}개`);
  console.log(`   스킵됨: ${skipped}개`);
  console.log(`   총합: ${created + skipped}개`);
  console.log('\n✨ 콘텐츠 생성 완료!');
}

// 스크립트 실행
main().catch(console.error);
