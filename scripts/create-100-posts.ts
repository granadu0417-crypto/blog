/**
 * 마크다운 포스트 파일 생성
 */
function generateMarkdownFile(post: KeywordPost): string {
  const contentGenerator = categoryTemplates[post.category];
  const content = contentGenerator ? contentGenerator(post) : generateDefaultContent(post);

  return `---
title: "${post.title}"
date: "${today}"
excerpt: "${post.excerpt}"
category: "${post.category}"
tags: ${JSON.stringify(post.tags)}
author: "kimyido"
---

${content}

---

**관련 글**: 더 많은 유용한 정보를 확인해보세요!
`;
}

